// ── Chat Session API ──────────────────────────────────────────────────────────

/** Upsert (save/update) a full chat session to MongoDB */
export function upsertChatSession(sessionData) {
  if (isDummy()) {
    const idx = dummyChatSessions.findIndex(s => s.session_id === sessionData.session_id)
    if (idx >= 0) dummyChatSessions[idx] = sessionData
    else dummyChatSessions.push(sessionData)
    return Promise.resolve(sessionData)
  }
  return request('/chat/sessions', {
    method: 'POST',
    headers: { 'Content-Type': 'application/json' },
    body: JSON.stringify(sessionData),
  })
}

/** Fetch all sessions for the logged-in user */
export function getChatSessions() {
  if (isDummy()) return Promise.resolve(dummyChatSessions)
  return request('/chat/sessions')
}

/** Fetch a single session by its session_id */
export function getChatSession(sessionId) {
  if (isDummy()) return Promise.resolve(dummyChatSessions.find(s => s.session_id === sessionId) || null)
  return request(`/chat/sessions/${sessionId}`)
}

/** Delete a session from MongoDB */
export function deleteChatSession(sessionId) {
  if (isDummy()) {
    dummyChatSessions = dummyChatSessions.filter(s => s.session_id !== sessionId)
    return Promise.resolve({ detail: 'Deleted' })
  }
  return request(`/chat/sessions/${sessionId}`, { method: 'DELETE' })
}

