export function exportSummary(filename) {
  const token = localStorage.getItem('mutant_token')
  const base = import.meta.env.VITE_API_URL || 'http://localhost:8000'
  const url = `${base}/summarize/export?filename=${encodeURIComponent(filename)}`
  const a = document.createElement('a')
  a.href = url
  const headers = new Headers()
  if (token) headers.set('Authorization', `Bearer ${token}`)
  fetch(url, { headers })
    .then(r => r.blob())
    .then(blob => {
      const objectUrl = URL.createObjectURL(blob)
      a.href = objectUrl
      a.download = `summary_${filename}.txt`
      document.body.appendChild(a)
      a.click()
      document.body.removeChild(a)
      URL.revokeObjectURL(objectUrl)
    })
    .catch(console.error)
}

export function categorizeDoc(filename) {
  if (isDummy()) {
    return new Promise((resolve) => {
      const categoryOptions = ['Finance', 'HR', 'Management', 'Legal', 'IT / Infra'];
      const randomCategory = categoryOptions[Math.floor(Math.random() * categoryOptions.length)];
      
      const updatedIdx = dummyDocuments.findIndex(d => d.filename === filename);
      if (updatedIdx >= 0) {
        dummyDocuments[updatedIdx] = {
          ...dummyDocuments[updatedIdx],
          category: randomCategory,
          status: 'indexed'
        };
      }
      setTimeout(() => resolve({ category: randomCategory, confidence: 0.95, extracted_fields: ['Mock: indexed'] }), 500)
    })
  }

  return request('/categorize', {
    method: 'POST',
    headers: { 'Content-Type': 'application/json' },
    body: JSON.stringify({ filename }),
  })
}

